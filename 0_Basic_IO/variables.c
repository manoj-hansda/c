// variables are used to store values of specific type.

#include <stdio.h>

int main()
{
    int a;      // 'a' variable can store values of integer(digits) type

    a = 5;      // assigning value '5' to variable 'a'

    printf("%d", a);    // %d is a placeholder (format specifier) for digits(integers)

    getch();
}
